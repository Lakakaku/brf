import { execSync } from 'child_process';
import { writeFileSync, readFileSync } from 'fs';
import path from 'path';

interface TestResult {
  numPassedTests: number;
  numFailedTests: number;
  numTotalTests: number;
  testResults: Array<{
    testFilePath: string;
    numPassingTests: number;
    numFailingTests: number;
    numTotalTests: number;
    duration: number;
  }>;
  success: boolean;
  startTime: number;
  endTime: number;
}

async function runIsolationTests() {
  console.log('\nðŸ”’ BRF Portal Database Isolation Test Suite');
  console.log('='.repeat(50));
  
  try {
    const startTime = Date.now();
    
    // Run the isolation tests with Jest
    console.log('ðŸ“‹ Running isolation tests...\n');
    
    const jestCommand = [
      'npx jest',
      '--testPathPattern=tests/database',
      '--verbose',
      '--coverage',
      '--json',
      '--outputFile=test-results.json'
    ].join(' ');
    
    const testOutput = execSync(jestCommand, { 
      encoding: 'utf-8',
      stdio: ['inherit', 'pipe', 'pipe']
    });
    
    console.log(testOutput);
    
    const endTime = Date.now();
    const duration = endTime - startTime;
    
    // Read the test results
    const resultsFile = path.join(process.cwd(), 'test-results.json');
    let testResults: TestResult;
    
    try {
      const resultsData = readFileSync(resultsFile, 'utf-8');
      testResults = JSON.parse(resultsData);
    } catch (error) {
      console.error('âŒ Could not read test results file');
      process.exit(1);
    }
    
    // Generate summary report
    generateSummaryReport(testResults, duration);
    
    // Generate detailed report
    await generateDetailedReport(testResults, duration);
    
    console.log('\nâœ… Isolation tests completed successfully!');
    console.log(`ðŸ“Š Results: ${testResults.numPassedTests}/${testResults.numTotalTests} tests passed`);
    console.log(`â±ï¸  Duration: ${duration}ms`);
    console.log(`ðŸ“„ Reports generated in tests/database/`);
    
    if (testResults.numFailedTests > 0) {
      console.log(`\nâš ï¸  ${testResults.numFailedTests} tests failed - see detailed report`);
      process.exit(1);
    }
    
  } catch (error) {
    console.error('\nâŒ Error running isolation tests:', error);
    
    if (error.stdout) {
      console.log('\nTest output:');
      console.log(error.stdout.toString());
    }
    
    if (error.stderr) {
      console.error('\nTest errors:');
      console.error(error.stderr.toString());
    }
    
    process.exit(1);
  }
}

